                // Email validation
                const emailLinks = document.querySelectorAll('a[href^="mailto:"]');
                emailLinks.forEach(link => {
                    link.addEventListener('click', function(e) {
                        const email = this.href.replace('mailto:', '');
                        if (!this.isValidEmail(email)) {
                            e.preventDefault();
                            console.warn('Invalid email address:', email);
                            this.showNotification('Invalid email address', 'error');
                        } else {
                            this.showNotification('Opening email client...', 'info');
                        }
                    }.bind(this));
                });

                // Phone validation
                const phoneLinks = document.querySelectorAll('a[href^="tel:"]');
                phoneLinks.forEach(link => {
                    link.addEventListener('click', function(e) {
                        const phone = this.href.replace('tel:', '');
                        console.log('Initiating call to:', phone);
                        this.showNotification('Initiating call...', 'info');
                    }.bind(this));
                });
            }

            setupVisibilityAnimations() {
                // Intersection Observer for scroll animations
                const observerOptions = {
                    threshold: 0.1,
                    rootMargin: '0px 0px -50px 0px'
                };

                const observer = new IntersectionObserver((entries) => {
                    entries.forEach(entry => {
                        if (entry.isIntersecting) {
                            entry.target.style.opacity = '1';
                            entry.target.style.transform = 'translateY(0)';
                        }
                    });
                }, observerOptions);

                // Observe footer sections
                const sections = [
                    '.footer_responsive_top_section',
                    '.footer_responsive_content_section',
                    '.footer_responsive_bottom_section'
                ];

                sections.forEach(selector => {
                    const element = document.querySelector(selector);
                    if (element) {
                        element.style.opacity = '0';
                        element.style.transform = 'translateY(30px)';
                        element.style.transition = 'opacity 0.8s ease, transform 0.8s ease';
                        observer.observe(element);
                    }
                });
            }

            setupResponsiveHandlers() {
                // Advanced responsive adjustments
                const handleResize = () => {
                    const windowWidth = window.innerWidth;
                    const brandBg = document.querySelector('.footer_responsive_brand_bg');
                    const footer = document.querySelector('.footer_responsive_main');

                    // Dynamic brand background sizing
                    if (windowWidth <= 360) {
                        brandBg.style.fontSize = '2rem';
                        brandBg.style.bottom = '-10px';
                    } else if (windowWidth <= 480) {
                        brandBg.style.fontSize = '2.5rem';
                        brandBg.style.bottom = '-15px';
                    } else if (windowWidth <= 576) {
                        brandBg.style.fontSize = '3rem';
                        brandBg.style.bottom = '-20px';
                    } else if (windowWidth <= 768) {
                        brandBg.style.fontSize = '4rem';
                        brandBg.style.bottom = '-30px';
                    } else if (windowWidth <= 992) {
                        brandBg.style.fontSize = '5rem';
                        brandBg.style.bottom = '-40px';
                    } else {
                        brandBg.style.fontSize = '';
                        brandBg.style.bottom = '';
                    }

                    // Adjust footer height for mobile
                    if (windowWidth <= 768) {
                        footer.style.minHeight = 'auto';
                    } else {
                        footer.style.minHeight = '100vh';
                    }
                };

                // Debounced resize handler
                let resizeTimer;
                window.addEventListener('resize', () => {
                    clearTimeout(resizeTimer);
                    resizeTimer = setTimeout(handleResize, 250);
                });

                // Initial call
                handleResize();
            }

            setupAccessibility() {
                // Enhanced keyboard navigation
                const focusableElements = document.querySelectorAll(
                    '.footer_responsive_main a, .footer_responsive_main button'
                );

                focusableElements.forEach((element, index) => {
                    element.addEventListener('keydown', (e) => {
                        switch (e.key) {
                            case 'Enter':
                            case ' ':
                                if (element.tagName === 'A')
                                    return; // Let default behavior handle links
                                e.preventDefault();
                                element.click();
                                break;
                            case 'Tab':
                                // Enhanced tab navigation
                                if (e.shiftKey && index === 0) {
                                    e.preventDefault();
                                    focusableElements[focusableElements.length - 1].focus();
                                } else if (!e.shiftKey && index === focusableElements.length - 1) {
                                    e.preventDefault();
                                    focusableElements[0].focus();
                                }
                                break;
                        }
                    });
                });

                // Screen reader announcements
                this.announceFooterLoad();
            }

