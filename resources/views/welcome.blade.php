        // FAQ Accordion Functionality
        class FAQAccordion {
            constructor() {
                this.questions = document.querySelectorAll('.faq_section_question');
                this.init();
            }

            init() {
                this.questions.forEach((question, index) => {
                    question.addEventListener('click', (e) => this.toggleQuestion(e, index));
                    question.addEventListener('keydown', (e) => this.handleKeydown(e, index));
                });

                // Initialize loading animation
                setTimeout(() => {
                    document.querySelector('.faq_section_accordion').classList.remove('faq_section_loading');
                }, 100);
            }

            toggleQuestion(e, index) {
                e.preventDefault();
                const question = e.currentTarget;
                const answer = question.nextElementSibling;
                const isActive = question.classList.contains('active');

                // Close all other questions (optional - remove for multiple open)
                // this.closeAllQuestions();

                if (isActive) {
                    this.closeQuestion(question, answer);
                } else {
                    this.openQuestion(question, answer);
                }

                // Announce to screen readers
                this.announceChange(question, !isActive);
            }

            openQuestion(question, answer) {
                question.classList.add('active');
                question.setAttribute('aria-expanded', 'true');
                answer.classList.add('active');

                // Smooth scroll to question if needed
                setTimeout(() => {
                    if (window.innerWidth <= 768) {
                        question.scrollIntoView({
                            behavior: 'smooth',
                            block: 'nearest'
                        });
                    }
                }, 300);
            }

            closeQuestion(question, answer) {
                question.classList.remove('active');
                question.setAttribute('aria-expanded', 'false');
                answer.classList.remove('active');
            }

            closeAllQuestions() {
                this.questions.forEach(question => {
                    const answer = question.nextElementSibling;
                    this.closeQuestion(question, answer);
                });
            }

            handleKeydown(e, index) {
                const key = e.key;

                switch (key) {
                    case 'Enter':
                    case ' ':
                        e.preventDefault();
                        this.toggleQuestion(e, index);
                        break;
                    case 'ArrowDown':
                        e.preventDefault();
                        this.focusNext(index);
                        break;
                    case 'ArrowUp':
                        e.preventDefault();
                        this.focusPrevious(index);
                        break;
                    case 'Home':
                        e.preventDefault();
                        this.questions[0].focus();
                        break;
                    case 'End':
                        e.preventDefault();
                        this.questions[this.questions.length - 1].focus();
                        break;
                }
            }

            focusNext(currentIndex) {
                const nextIndex = (currentIndex + 1) % this.questions.length;
                this.questions[nextIndex].focus();
            }

            focusPrevious(currentIndex) {
                const prevIndex = currentIndex === 0 ? this.questions.length - 1 : currentIndex - 1;
                this.questions[prevIndex].focus();
            }

            announceChange(question, isOpen) {
                const announcement = document.createElement('div');
                announcement.setAttribute('aria-live', 'polite');
                announcement.setAttribute('aria-atomic', 'true');
                announcement.className = 'sr-only';

                const questionText = question.querySelector('.faq_section_question_text').textContent.trim();
                announcement.textContent = `${questionText} ${isOpen ? 'expanded' : 'collapsed'}`;

                document.body.appendChild(announcement);

                setTimeout(() => {
                    if (document.body.contains(announcement)) {
                        document.body.removeChild(announcement);
                    }
                }, 1000);
            }
        }

        // Image lazy loading and error handling
        function initializeImages() {
            const images = document.querySelectorAll('.faq_section_image');

            images.forEach(image => {
                image.addEventListener('load', function() {
                    this.style.opacity = '1';
                });

                image.addEventListener('error', function() {
                    console.warn('Image failed to load:', this.src);
                    this.style.backgroundColor = '#f8f9fa';
                    this.alt = 'Image not available';
                });
            });
        }

        // Intersection Observer for animations
        function initializeAnimations() {
            const observerOptions = {
                threshold: 0.1,
                rootMargin: '0px 0px -50px 0px'
            };

            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.style.opacity = '1';
                        entry.target.style.transform = 'translateY(0)';
                    }
                });
            }, observerOptions);

            // Observe FAQ items
            document.querySelectorAll('.faq_section_item').forEach(item => {
                item.style.opacity = '0';
                item.style.transform = 'translateY(20px)';
                item.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
                observer.observe(item);
            });
        }

        // Responsive adjustments
        function handleResize() {
            const images = document.querySelectorAll('.faq_section_image_left, .faq_section_image_right');

            if (window.innerWidth <= 992) {
                images.forEach(img => {
                    img.style.position = 'static';
                });
            } else {
                images.forEach(img => {
                    img.style.position = 'sticky';
                });
            }
        }

